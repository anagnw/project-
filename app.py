# ─── Helpers ───────────────────────────────────────────────────────────────────
def calculate_anomalies(year_data):
    result = []
    for sector, v in year_data["τομείς"].items():
        if v["ψηφισθέν"] == 0:  # Αποφυγή ZeroDivisionError για ανύπαρκτα έτη του Υπ. Ψηφιακής Διακυβέρνησης
            continue
        diff = v["εκτελεσθέν"] - v["ψηφισθέν"]
        pct  = (diff / v["ψηφισθέν"]) * 100
        
        if abs(pct) >= 8.0:
            severity = "high"
        elif abs(pct) >= 3.5:
            severity = "medium"
        else:
            severity = "low"
            
        result.append({"τομέας": sector, "ψηφισθέν": v["ψηφισθέν"],
                        "εκτελεσθέν": v["εκτελεσθέν"], "διαφορά": diff,
                        "ποσοστό": pct, "severity": severity})
    return sorted(result, key=lambda x: abs(x["ποσοστό"]), reverse=True)

def badge(severity):
    labels = {"high": "🔴 Υψηλή", "medium": "🟡 Μέτρια", "low": "🟢 Χαμηλή"}
    classes = {"high": "badge-high", "medium": "badge-medium", "low": "badge-low"}
    return labels[severity], classes[severity]

# ─── PDF Generation Function ──────────────────────────────────────────────────
def generate_pdf_bytes(selected_year, y_data, filtered_anomalies, ai_txt):
    pdf = FPDF()
    pdf.add_page()
    greek_to_slug = {
        'Α': 'A', 'Β': 'B', 'Γ': 'G', 'Δ': 'D', 'Ε': 'E', 'Ζ': 'Z', 'Η': 'I', 'Θ': 'TH', 'Ι': 'I', 'Κ': 'K', 'Λ': 'L', 'Μ': 'M', 'Ν': 'N', 'Ξ': 'X', 'Ο': 'O', 'Π': 'P', 'Ρ': 'R', 'Σ': 'S', 'Τ': 'T', 'Υ': 'Y', 'Φ': 'PH', 'Χ': 'CH', 'Ψ': 'PS', 'Ω': 'O',
        'α': 'a', 'β': 'b', 'γ': 'g', 'δ': 'd', 'ε': 'e', 'ζ': 'z', 'η': 'i', 'θ': 'th', 'ι': 'i', 'κ': 'k', 'λ': 'l', 'μ': 'm', 'ν': 'n', 'ξ': 'x', 'ο': 'o', 'π': 'p', 'ρ': 'r', 'σ': 's', 'τ': 't', 'υ': 'y', 'φ': 'ph', 'χ': 'ch', 'ψ': 'ps', 'ω': 'o',
        'έ': 'e', 'ά': 'a', 'ή': 'i', 'ί': 'i', 'ό': 'o', 'ώ': 'o', 'ύ': 'y', 'ς': 's', ',': '', ' ': '_'
    }
    def to_ascii(text): return "".join(greek_to_slug.get(c, c) for c in text)

    pdf.set_font("Helvetica", "B", 16)
    pdf.cell(0, 10, f"OFFICIAL BUDGET ANOMALY REPORT - YEAR {selected_year}", new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(5)
    pdf.set_font("Helvetica", "", 12)
    label_type = "Primary Surplus" if y_data['πρωτογενές_ισοζύγιο'] >= 0 else "Primary Deficit"
    pdf.cell(0, 10, f"{label_type}: {y_data['πρωτογενές_ισοζύγιο']}% of GDP | Debt: {y_data['χρέος']}% of GDP", new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(10)
    pdf.set_font("Helvetica", "B", 11)
    pdf.cell(60, 8, "Ministry / Entity", 1)
    pdf.cell(35, 8, "Budgeted (M)", 1)
    pdf.cell(35, 8, "Executed (M)", 1)
    pdf.cell(30, 8, "Dev. %", 1)
    pdf.cell(30, 8, "Severity", 1, new_x="LMARGIN", new_y="NEXT")
    pdf.set_font("Helvetica", "", 10)
    for a in filtered_anomalies:
        pdf.cell(60, 8, to_ascii(a['τομέας'])[:28], 1)
        pdf.cell(35, 8, f"{a['ψηφισθέν']:,} EUR", 1)
        pdf.cell(35, 8, f"{a['εκτελεσθέν']:,} EUR", 1)
        pdf.cell(30, 8, f"{a['ποσοστό']:+.1f}%", 1)
        pdf.cell(30, 8, str(a['severity'].upper()), 1, new_x="LMARGIN", new_y="NEXT")
    pdf.ln(10)
    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(0, 8, "AI Budgetary Analysis & Comments:", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(2)
    pdf.set_font("Helvetica", "", 10)
    pdf.multi_cell(0, 6, to_ascii(ai_txt))
    return pdf.output()

